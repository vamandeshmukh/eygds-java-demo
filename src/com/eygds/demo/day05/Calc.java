package com.eygds.demo.day05;

public class Calc {

	public static int addNums(int i, int j) {
		return i + j;
	}

	public static int addNums(int i, int j, int k) {
		return i + j + k;
	}

	public static int addNums(int i, int j, int k, int l) {
		return i + j + k + l;
	}

	public static int addNums(int i, short j) {
		return i + j;
	}

	public static int addNums(short i, int j) {
		return i + j;
	}

}
